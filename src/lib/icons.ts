// ── Public API ──────────────────────────────────────────────────────

type StrokedIconName = keyof typeof STROKED;
type FilledIconName = keyof typeof FILLED;

interface IconOpts {
  size?: number;
  stroke?: string;
  strokeWidth?: number;
}

interface FilledIconOpts {
  size?: number;
  fill?: string;
}

export function icon(
  name: StrokedIconName,
  opts: IconOpts = {},
): string {
  const { size = 24, stroke = 'currentColor', strokeWidth = 1.5 } = opts;
  const paths = STROKED[name];
  if (!paths) return '';
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="${stroke}" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round">${paths}</svg>`;
}

export function filledIcon(
  name: FilledIconName,
  opts: FilledIconOpts = {},
): string {
  const { size = 32, fill = 'currentColor' } = opts;
  const paths = FILLED[name];
  if (!paths) return '';
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="${fill}">${paths}</svg>`;
}
